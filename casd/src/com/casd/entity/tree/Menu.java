package com.casd.entity.tree;

import java.io.Serializable;
import java.util.List;

public class Menu implements Serializable {

	
	private int id; 			//菜单id 
	private String menu_name;	//菜单名
	private int permit_id; 		//权限id 
	private int parent_id; 		//父菜单id
	private String menu_url;	//菜单url
	private int order;			//菜单顺序
	private int ismenu;			//是否菜单项
	private String program_code;  //权限标签 code
	
	
    private List<Menu> childMenus;// 子菜单
    
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getMenu_name() {
		return menu_name;
	}
	public void setMenu_name(String menu_name) {
		this.menu_name = menu_name;
	}
	public int getPermit_id() {
		return permit_id;
	}
	public void setPermit_id(int permit_id) {
		this.permit_id = permit_id;
	}
	public int getParent_id() {
		return parent_id;
	}
	public void setParent_id(int parent_id) {
		this.parent_id = parent_id;
	}
	public String getMenu_url() {
		return menu_url;
	}
	public void setMenu_url(String menu_url) {
		this.menu_url = menu_url;
	}
	public int getOrder() {
		return order;
	}
	public void setOrder(int order) {
		this.order = order;
	}
	public List<Menu> getChildMenus() {
		return childMenus;
	}
	public void setChildMenus(List<Menu> childMenus) {
		this.childMenus = childMenus;
	}
	public int getIsmenu() {
		return ismenu;
	}
	public void setIsmenu(int ismenu) {
		this.ismenu = ismenu;
	}
	public String getProgram_code() {
		return program_code;
	}
	public void setProgram_code(String program_code) {
		this.program_code = program_code;
	}
	
}
